package org.emona.model.types;

public class Flag {
	
	private String token;
	private int value;
	private String description;
	
	public Flag(String token, int value) {
		this.token = token;
		this.value = value;
		this.description = "";
	}

	public Flag(String token, int value, String description) {
		this.token = token;
		this.value = value;
		this.description = description;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}
}